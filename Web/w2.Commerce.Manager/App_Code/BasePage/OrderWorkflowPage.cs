	/// <summary>
	/// 定期購入商品単価取得取得
	/// </summary>
	/// <param name="fixedPurchasePrice">定期購入価格</param>
	/// <param name="memberRankPrice">会員ランク価格</param>
	/// <param name="specialPrice">特別価格</param>
	/// <param name="price">購入価格</param> 
	/// <returns>定期購入商品単価</returns>
	public static string GetFixedPurchaseItemPrice(
		string fixedPurchasePrice, 
		string memberRankPrice,
		string specialPrice,
		string price)
	{
		if (string.IsNullOrEmpty(fixedPurchasePrice) == false) return fixedPurchasePrice;
		if (string.IsNullOrEmpty(memberRankPrice) == false) return memberRankPrice;
		if (string.IsNullOrEmpty(specialPrice) == false) return specialPrice;
		return price;
	}

	/// <summary>
	/// 定期購入商品の価格を計算(単価*数量)
	/// </summary>
	/// <param name="fixedPurchaseItemPrice">定期購入商品単価</param>
	/// <param name="itemQuantity">会員購入商品個数</param>
	/// <returns>定期購入商品合計価格</returns>
	public static decimal CaluculateFixedPurchaseItemPriceSum(string fixedPurchaseItemPrice, string itemQuantity)
	{
		int quantity;
		int.TryParse(itemQuantity, out quantity);
		Decimal price;
		Decimal.TryParse(fixedPurchaseItemPrice, out price);
		var priceSum = quantity * price;
		return priceSum;
	}

