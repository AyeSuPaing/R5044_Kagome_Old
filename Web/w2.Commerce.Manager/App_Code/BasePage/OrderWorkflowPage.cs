	/// <summary>
	/// 注文ワークフロー設定情報データビュー取得
	/// </summary>
	/// <param name="strShopId">店舗ID</param>
	/// <param name="strWorkFlowKbn">ワークフロー区分</param>
	/// <param name="strWorkFlowNo">枝番</param>
	/// <returns>注文ワークフロー設定情報データビュー</returns>
	protected DataView GetOrderWorkflowSetting(string strShopId, string strWorkFlowKbn, string strWorkFlowNo)
	{
		string pageName = null;
		string statementName = null;
		if (this.RequestWorkflowType == KBN_WORKFLOW_TYPE_ORDER)
		{
			pageName = "OrderWorkflowSetting";
			statementName = "GetOrderWorkflowSetting";
		}
		else
		{
			pageName = "FixedPurchaseWorkflowSetting";
			statementName = "GetFixedPurchaseWorkflowSetting";
		}
		using (var accessor = new SqlAccessor())
		using (var statement = new SqlStatement(pageName, statementName))
		{
			var input = new Hashtable
			{
				{ Constants.FIELD_ORDERWORKFLOWSETTING_SHOP_ID, strShopId },
				{ Constants.FIELD_ORDERWORKFLOWSETTING_WORKFLOW_KBN, strWorkFlowKbn },
				{ Constants.FIELD_ORDERWORKFLOWSETTING_WORKFLOW_NO, strWorkFlowNo }
			};

			return statement.SelectSingleStatementWithOC(accessor, input);
		}
	}

	/// <summary>
	/// ワークフロー設定一覧遷移URL作成
	/// </summary>
	/// <param name="searchParameters">検索情報</param>
	/// <param name="isAddPageNumber">ページを付けるかどうかフラグ（ページャ作成の際はfalse）</param>
	/// <param name="pageNumber">ページ番号</param>
	/// <returns>ワークフロー設定一覧遷移URL</returns>
	protected string CreatOrderWorkflowSettingListUrl(Hashtable searchParameters, bool isAddPageNumber, int? pageNumber)
	{
		var url = new UrlCreator(Constants.PATH_ROOT + Constants.PAGE_MANAGER_ORDERWORKFLOWSETTING_LIST)
			.AddParam(Constants.REQUEST_KEY_ORDERWORKFLOWSETTING_WORKFLOW_KBN, HttpUtility.UrlEncode((string)searchParameters[Constants.REQUEST_KEY_ORDERWORKFLOWSETTING_WORKFLOW_KBN]))
			.AddParam(Constants.REQUEST_KEY_ORDERWORKFLOWSETTING_WORKFLOW_NAME, StringUtility.ToEmpty(searchParameters[Constants.REQUEST_KEY_ORDERWORKFLOWSETTING_WORKFLOW_NAME]))
			.AddParam(Constants.REQUEST_KEY_ORDERWORKFLOWSETTING_WORKFLOW_VALID_FLG, HttpUtility.UrlEncode((string)searchParameters[Constants.REQUEST_KEY_ORDERWORKFLOWSETTING_WORKFLOW_VALID_FLG]))
			.AddParam(Constants.REQUEST_KEY_ORDERWORKFLOWSETTING_WORKFLOW_DETAIL_KBN, HttpUtility.UrlEncode((string)searchParameters[Constants.REQUEST_KEY_ORDERWORKFLOWSETTING_WORKFLOW_DETAIL_KBN]))
			.AddParam(Constants.REQUEST_KEY_ORDERWORKFLOWSETTING_WORKFLOW_ADDITIONAL_SEARCH_FLG, HttpUtility.UrlEncode((string)searchParameters[Constants.REQUEST_KEY_ORDERWORKFLOWSETTING_WORKFLOW_ADDITIONAL_SEARCH_FLG]))
			.AddParam(Constants.REQUEST_KEY_SORT_KBN, HttpUtility.UrlEncode((string)searchParameters[Constants.REQUEST_KEY_SORT_KBN]));

		if (isAddPageNumber)
		{
			url.AddParam(Constants.REQUEST_KEY_PAGE_NO, HttpUtility.UrlEncode(
				pageNumber.HasValue
				? pageNumber.ToString()
				: searchParameters[Constants.REQUEST_KEY_PAGE_NO].ToString()));
		}

		return url.CreateUrl();
	}

