	/// </summary>
	/// <returns>注文ワークフロー設定数がユーザー利用可能最大件数より少ないか</returns>
	protected bool IsLessOrderWorkflowSettingThenMaxCount()
	{
		var isLess = ((Constants.ORDERWORKFLOWSETTING_MAXCOUNT.HasValue == false)
			|| (Constants.ORDERWORKFLOWSETTING_MAXCOUNT <= 0)
			|| (this.OrderWorkflowSettingItemsCount < Constants.ORDERWORKFLOWSETTING_MAXCOUNT));
		return isLess;
	}

